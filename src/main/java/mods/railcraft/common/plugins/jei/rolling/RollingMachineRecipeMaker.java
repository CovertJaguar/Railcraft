package mods.railcraft.common.plugins.jei.rolling;

import mezz.jei.api.IJeiHelpers;
import mods.railcraft.api.crafting.RailcraftCraftingManager;

import java.util.List;
import java.util.stream.Collectors;

public class RollingMachineRecipeMaker {

    public static List<RollingMachineRecipeWrapper> getRecipes(IJeiHelpers jeiHelpers) {
        return RailcraftCraftingManager.rollingMachine.getRecipeList().stream().map(recipe -> new RollingMachineRecipeWrapper(jeiHelpers, recipe)).collect(Collectors.toList());
    }

}
